# Basic Operators
a = 10
b = 5

# Arithmetic Operators
sum_result = a + b
difference = a - b
product = a * b
quotient = a / b

# Comparison Operators
is_equal = (a == b)
is_greater = (a > b)

# Logical Operators
logical_and = (a > 5 and b < 10)
logical_or = (a < 5 or b > 3)

print(f"Sum: {sum_result}, Difference: {difference}, Product: {product}, Quotient: {quotient}")
print(f"Is Equal: {is_equal}, Is Greater: {is_greater}")
print(f"Logical AND: {logical_and}, Logical OR: {logical_or}")
