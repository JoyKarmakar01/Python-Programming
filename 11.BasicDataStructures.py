# Lists
my_list = [1, 2, 3, 4, 5]
my_list.append(6)
print(f"List: {my_list}")

# Tuples
my_tuple = (1, 2, 3, 4, 5)
print(f"Tuple: {my_tuple}")

# Sets
my_set = {1, 2, 3, 4, 5}
my_set.add(6)
print(f"Set: {my_set}")

# Dictionaries
my_dict = {"name": "Alice", "age": 25}
my_dict["city"] = "New York"
print(f"Dictionary: {my_dict}")
